package Demo;
//练习： 打印1-5，正着打印一遍，反着打印一遍
public class Exercise1 {
    public static void main(String[] args) {
        //打印1-5
        for (int i = 1;i<6;i++) {
            System.out.println(i);
        }
        //打印5-1
        for (int j = 5;j>0;j--) {
            System.out.println(j);
        }

    }
}
